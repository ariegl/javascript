
/**
 * Realiza la suma de dos numeros
 * @function
 * @param {number} a - El primer numero 
 * @param {number} b - El segundo numero
 * @returns {number} La suma de A y B
 */
function suma(a,b) {
    return a+b;
}

/**
 *Resultado de la funcion suma 
 *  @type {number}
 */
const resultado = suma(10,5);

